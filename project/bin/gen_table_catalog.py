#!/usr/bin/env python3
"""Generate docs/tables.md and docs/openapi-schemas.yaml from build_sqlite.SCHEMAS.

This script is build-time only ŌĆö it is NOT included in the runtime Docker image.
Developers run it after editing the SCHEMAS dict in build_sqlite.py to refresh
the per-table documentation. CI runs it in --check mode to ensure committed
docs match the current SCHEMAS.

Usage:
  python3 project/bin/gen_table_catalog.py             # regenerate docs/*
  python3 project/bin/gen_table_catalog.py --check     # nonzero exit if regen would change anything

The generator reads SCHEMAS from build_sqlite.py and the table groupings
(TASK_TABLES, REFERENCE_TABLES, CACHE_5MIN, HR_PARENT_COLUMN) from server.py.
Column types are inferred by inspecting each lambda's source. Only three
patterns are recognised; anything else raises so SCHEMAS edits surface here.
"""
import argparse
import inspect
import re
import sys
from pathlib import Path

HERE = Path(__file__).resolve().parent
sys.path.insert(0, str(HERE))

import build_sqlite                                              # noqa: E402
import server                                                    # noqa: E402

REPO_ROOT = HERE.parent.parent
DOCS = REPO_ROOT / 'docs'

GEN_BANNER_MD = (
    '<!-- AUTO-GENERATED by project/bin/gen_table_catalog.py. Do not edit. -->\n'
    '<!-- Source: project/bin/build_sqlite.py (SCHEMAS dict) -->\n'
)
GEN_BANNER_YAML = (
    '# AUTO-GENERATED by project/bin/gen_table_catalog.py. Do not edit.\n'
    '# Source: project/bin/build_sqlite.py (SCHEMAS dict)\n'
)


# --- type inference ---------------------------------------------------------

_BOOL_INT_RE = re.compile(
    r"1\s+if\s+str\(\s*_v\(.*?\)\s*or\s*['\"]false['\"]\s*\)\.lower\(\)\s*==\s*['\"]true['\"]\s+else\s+0",
    re.DOTALL,
)
_DV_RE = re.compile(r"\b_dv\s*\(")
_V_RE = re.compile(r"\b_v\s*\(")


def infer_type(lam):
    """Return ('bool-int' | 'string' | 'display-string', source_snippet).

    bool-int       ŌĆö extracted via `1 if str(_v(...)) ... else 0` (ServiceNow boolean)
    display-string ŌĆö extracted via `_dv(...)` (uses display_value instead of raw value)
    string         ŌĆö extracted via `_v(...)` (raw value)

    Raises ValueError on any unrecognised pattern so SCHEMAS edits force the
    generator to be updated rather than silently producing wrong types.
    """
    src = inspect.getsource(lam).strip()
    if _BOOL_INT_RE.search(src):
        return 'bool-int', src
    if _DV_RE.search(src):
        return 'display-string', src
    if _V_RE.search(src):
        return 'string', src
    raise ValueError(
        f'unrecognised schema lambda pattern in build_sqlite.SCHEMAS ŌĆö '
        f'extend gen_table_catalog.infer_type() to handle this case:\n'
        f'  {src}'
    )


# --- table grouping ---------------------------------------------------------

def tags_for(table):
    out = []
    if table in server.TASK_TABLES:
        out.append('task')
    if table in server.CACHE_5MIN:
        out.append('cache-5min')
    if table == 'incident' or table in server.HR_PARENT_COLUMN:
        out.append('hr-gated')
    if not out and table in server.REFERENCE_TABLES:
        out.append('reference')
    return out


def hr_parent_note(table):
    """If the table is HR-gated by a parent column, return a human-readable note."""
    if table == 'incident':
        return 'Directly gated: rows where `assignment_group` is the HR group are hidden when locked.'
    col = server.HR_PARENT_COLUMN.get(table)
    if col:
        return f'Parent-gated: rows whose `{col}` references an HR-assigned incident are hidden when locked.'
    return None


def cache_note(table):
    if table in server.CACHE_5MIN:
        note = (
            'Tagged `cache-5min`: list responses return `Cache-Control: public, max-age=300` '
            'when the `q` parameter is empty and `limit > 200`.'
        )
        if table == 'incident' or table in server.HR_PARENT_COLUMN:
            note += ' Public caching is disabled while the HR gate is configured because visibility varies by cookie.'
        return note
    return None


# --- catalog model ----------------------------------------------------------

def build_catalog():
    """Return [{table, tags, columns: [{name, type, note}], hr_note, cache_note}, ...]."""
    catalog = []
    for table, cols in build_sqlite.SCHEMAS.items():
        rows = []
        for col_name, lam in cols:
            kind, _src = infer_type(lam)
            note = None
            if kind == 'display-string':
                note = 'extracted from `display_value` (label form, e.g. "Active" rather than the underlying code)'
            elif kind == 'bool-int':
                note = 'ServiceNow boolean serialized as `"0"` or `"1"`; filter with `?col=true|false` (auto-coerced) or `?col=1|0`'
            rows.append({'name': col_name, 'type': kind, 'note': note})
        catalog.append({
            'table': table,
            'tags': tags_for(table),
            'columns': rows,
            'hr_note': hr_parent_note(table),
            'cache_note': cache_note(table),
        })
    return catalog


# --- tables.md renderer -----------------------------------------------------

def _tag_chip(tag):
    return f'`{tag}`'


def render_tables_md(catalog):
    out = [GEN_BANNER_MD, '']
    out.append('# Table catalog')
    out.append('')
    out.append('Every table here is queryable via `GET /api/<table>` (list, paginated) '
               'and `GET /api/<table>/<sys_id>` (single record). The columns listed are '
               'the **indexed** columns ŌĆö those are the only ones you can filter on '
               'with `?col=value`, and the table-list route returns them as scalars '
               'with `?slim=1`. Non-slim lists and single-record responses merge the '
               'stored `raw` JSON envelope on top; a same-named field can therefore '
               'retain its ServiceNow `{value, display_value}` object. The single-record '
               'route does not support slim mode.')
    out.append('')
    out.append('## Tag legend')
    out.append('')
    out.append('| Tag | Meaning |')
    out.append('| --- | --- |')
    out.append('| `task` | Descendant of ServiceNow\'s `task` base class. Shares core fields like `number`, `state`, `priority`, `assignment_group`. |')
    out.append('| `reference` | Reference / lookup / metadata table. |')
    out.append('| `cache-5min` | List responses set `Cache-Control: max-age=300` when `q` is empty and `limit > 200`. |')
    out.append('| `hr-gated` | Rows linked to HR-assigned incidents are hidden from list and record responses when the request lacks an `hr_unlock` cookie. See [API.md ┬¦3](API.md#3-authentication--authorization-hr-gate). |')
    out.append('')
    out.append('## Column type legend')
    out.append('')
    out.append('| Type | Meaning |')
    out.append('| --- | --- |')
    out.append('| `string` | Raw value (most columns). |')
    out.append('| `string (display)` | Display value (the human-friendly label rather than the underlying code). |')
    out.append('| `bool 0/1` | ServiceNow boolean serialized as string `"0"` or `"1"` in slim/indexed responses. Filter with `?col=true` or `?col=false` (auto-coerced) or `?col=1` / `?col=0`. |')
    out.append('')

    # Group tables: task ŌåÆ reference ŌåÆ activity ŌåÆ catalog ŌåÆ server-side logic ŌåÆ
    # assets/inventory. Keep insertion order from SCHEMAS within each group so
    # editors can predict where a new table will land in the doc.
    GROUP_ORDER = [
        ('Task tables', lambda t: t in server.TASK_TABLES),
        ('All other tables', lambda t: t not in server.TASK_TABLES),
    ]
    by_group = {label: [] for label, _ in GROUP_ORDER}
    for entry in catalog:
        for label, pred in GROUP_ORDER:
            if pred(entry['table']):
                by_group[label].append(entry)
                break

    for label, _ in GROUP_ORDER:
        entries = by_group[label]
        if not entries:
            continue
        out.append(f'## {label}')
        out.append('')
        for entry in entries:
            out.append(f"### `{entry['table']}`")
            out.append('')
            if entry['tags']:
                out.append('Tags: ' + ' '.join(_tag_chip(t) for t in entry['tags']))
                out.append('')
            if entry['hr_note']:
                out.append('> ' + entry['hr_note'])
                out.append('')
            if entry['cache_note']:
                out.append('> ' + entry['cache_note'])
                out.append('')
            out.append('Filterable columns:')
            out.append('')
            out.append('| Column | Type | Notes |')
            out.append('| --- | --- | --- |')
            out.append('| `sys_id` | string | Primary key; reference target for cross-table lookups. |')
            for col in entry['columns']:
                tname = {
                    'string': 'string',
                    'display-string': 'string (display)',
                    'bool-int': 'bool 0/1',
                }[col['type']]
                note = col['note'] or ''
                out.append(f"| `{col['name']}` | {tname} | {note} |")
            out.append('')
    return '\n'.join(out).rstrip() + '\n'


# --- openapi-schemas.yaml renderer ------------------------------------------

def _y_str(s):
    """YAML scalar string. Quotes if needed; otherwise plain."""
    if s is None:
        return '~'
    if isinstance(s, bool):
        return 'true' if s else 'false'
    if isinstance(s, (int, float)):
        return str(s)
    s = str(s)
    # Quote if contains anything yaml-special, leading/trailing whitespace, or
    # could parse as something else (numbers, booleans, null).
    needs_quote = (
        any(c in s for c in ':#&*!|>%@`{}[],?\n') or
        s.strip() != s or
        s == '' or
        s.lower() in ('true', 'false', 'null', 'yes', 'no', 'on', 'off', '~')
    )
    if not needs_quote:
        try:
            float(s)
            needs_quote = True
        except ValueError:
            pass
    if needs_quote:
        return "'" + s.replace("'", "''") + "'"
    return s


def render_openapi_schemas_yaml(catalog):
    out = [GEN_BANNER_YAML, '']
    out.append('components:')
    out.append('  schemas:')

    for entry in catalog:
        schema_name = _schema_name_for(entry['table'])
        tags_phrase = ', '.join(entry['tags']) if entry['tags'] else 'reference'
        desc = (
            f"Slim/indexed row shape from the `{entry['table']}` table ({tags_phrase}). "
            'In a non-slim response, a same-named raw field may instead retain its '
            'ServiceNow {value, display_value} envelope.'
        )
        if entry['hr_note']:
            desc += ' ' + entry['hr_note']
        out.append(f'    {schema_name}:')
        out.append(f'      type: object')
        out.append(f'      description: {_y_str(desc)}')
        out.append(f'      properties:')
        out.append(f'        sys_id:')
        out.append(f'          type: string')
        out.append(f'          description: ServiceNow primary key.')
        for col in entry['columns']:
            out.append(f"        {col['name']}:")
            if col['type'] == 'bool-int':
                out.append('          type: string')
                out.append("          enum: ['0', '1']")
                out.append('          description: ServiceNow boolean serialized as 0 or 1 in slim/indexed responses.')
            elif col['type'] == 'display-string':
                out.append('          type: string')
                out.append('          description: Display value (label form).')
            else:
                out.append('          type: string')
        out.append('      required: [sys_id]')
        out.append('      additionalProperties: true')

    # Machine-readable extensions at spec root.
    out.append('')
    out.append('x-filterable-columns:')
    for entry in catalog:
        cols = ['sys_id'] + [c['name'] for c in entry['columns']]
        out.append(f"  {entry['table']}: [{', '.join(cols)}]")

    out.append('')
    out.append('x-task-tables:')
    for table in sorted(server.TASK_TABLES):
        out.append(f'  - {table}')

    out.append('')
    out.append('x-cache-5min:')
    for table in sorted(server.CACHE_5MIN):
        out.append(f'  - {table}')

    out.append('')
    out.append('x-hr-parent-columns:')
    for table in sorted(server.HR_PARENT_COLUMN):
        out.append(f"  {table}: {server.HR_PARENT_COLUMN[table]}")

    return '\n'.join(out).rstrip() + '\n'


def _schema_name_for(table):
    """Camel-case a table name into a Row schema name. sys_user ŌåÆ SysUserRow."""
    return ''.join(p.capitalize() for p in table.split('_')) + 'Row'


def check_openapi_table_enum():
    """The /api/{table} enum in docs/openapi.yaml is hand-authored but must list
    exactly server.ALL_TABLES. It has drifted before (tables added to the server
    without updating the enum), so enforce it here. openapi.yaml isn't generated
    from SCHEMAS, so we report mismatches rather than rewrite the spec. Returns a
    list of human-readable problems (empty when in sync)."""
    spec = (REPO_ROOT / 'docs' / 'openapi.yaml').read_text(encoding='utf-8')
    m = re.search(r'/api/\{table\}:.*?\n\s+enum:\n((?:\s+-\s+\w+\n)+)', spec, re.DOTALL)
    if not m:
        return ['could not locate the /api/{table} enum in docs/openapi.yaml']
    listed = set(re.findall(r'-\s+(\w+)', m.group(1)))
    expected = set(server.ALL_TABLES)
    problems = []
    if expected - listed:
        problems.append('missing from enum: ' + ', '.join(sorted(expected - listed)))
    if listed - expected:
        problems.append('in enum but not served: ' + ', '.join(sorted(listed - expected)))
    return problems


# --- main -------------------------------------------------------------------

def main():
    parser = argparse.ArgumentParser(description=__doc__.split('\n', 1)[0])
    parser.add_argument(
        '--check', action='store_true',
        help='Exit nonzero if regenerating would change tables.md or openapi-schemas.yaml. CI uses this.',
    )
    args = parser.parse_args()

    catalog = build_catalog()
    md = render_tables_md(catalog)
    yml = render_openapi_schemas_yaml(catalog)

    targets = [
        (DOCS / 'tables.md', md),
        (DOCS / 'openapi-schemas.yaml', yml),
    ]
    enum_problems = check_openapi_table_enum()

    if args.check:
        drift = []
        for path, content in targets:
            existing = path.read_text(encoding='utf-8') if path.is_file() else ''
            if existing != content:
                drift.append(path)
        if drift or enum_problems:
            if drift:
                paths = ', '.join(str(p.relative_to(REPO_ROOT)) for p in drift)
                print(
                    f'gen_table_catalog: {paths} is out of date. '
                    'Run `make docs` (or `python3 project/bin/gen_table_catalog.py`) and commit the result.',
                    file=sys.stderr,
                )
            for p in enum_problems:
                print(f'gen_table_catalog: docs/openapi.yaml /api/{{table}} enum {p}', file=sys.stderr)
            sys.exit(1)
        print('gen_table_catalog: all docs are up to date.')
        return

    DOCS.mkdir(parents=True, exist_ok=True)
    for path, content in targets:
        path.write_text(content, encoding='utf-8')
    for p in enum_problems:
        print(f'gen_table_catalog: WARNING ŌĆö docs/openapi.yaml /api/{{table}} enum {p}', file=sys.stderr)
        print(f'wrote {path.relative_to(REPO_ROOT)} ({len(content):,} bytes)')


if __name__ == '__main__':
    main()
